CREATE TABLE IF NOT EXISTS stg.returns
(
    order_id character varying(20) COLLATE pg_catalog."default",
    return_id character varying(20) COLLATE pg_catalog."default",
    itemcharacter character varying(10) COLLATE pg_catalog."default",
    quantity integer,
    movement_id integer,
    from_location character varying(50) COLLATE pg_catalog."default",
    to_location character varying(50) COLLATE pg_catalog."default",
    received_by character varying(50) COLLATE pg_catalog."default",
    date date
)
